package com.corejava.chaptr4;

public class OperatorExample {
	public static void main (String  [] args){
		int a = 50;
		int b = 40+ a ;
		// arithmetic
	System.out.println(b);
	// Relational opperator
		System.out.println(50 < 4);
		// logical operator
		System.out.println(50 > 40 && 40 > 50);
		System.out.println(50 > 40 || 40 > 50);
	}

}
