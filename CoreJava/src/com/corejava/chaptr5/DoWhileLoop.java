package com.corejava.chaptr5;

public class DoWhileLoop {
	public static void main(String[] args){
		int i = 1;

		// creating loop to print the va1ue of i 10 times
		do {
			System.out.println(i);
			i++;
		}
		while(i<=10); // this loop will execute until the condition is true
	}

}

// Syntax 

//  do { logic we want to perform repeatedly}
//  while (condition to check true or false);
